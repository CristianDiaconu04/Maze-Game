# Takes 2 Cells and draws a line inbetween them
# The requirement is that cell1 and cell2 are right next to eachother 
def drawLine(cell1, cell2, screen):
    if cell1.y == cell2.y:
        # cells are to the right or to the left of eachother
        if cell1.x < cell2.x: 
            # cell1 is to the left of cell2
            pygame.draw.rect(screen, (0, 0, 0), (cell1.x * CELL_SIDE_LENGTH + CELL_SIDE_LENGTH, cell1.y * CELL_SIDE_LENGTH, 1, CELL_SIDE_LENGTH))
            cell1.wallRight = True
            cell2.wallLeft = True
        else:
            # cell1 is to the right of cell2
            pygame.draw.rect(screen, (0, 0, 0), (cell2.x * CELL_SIDE_LENGTH + CELL_SIDE_LENGTH, cell2.y * CELL_SIDE_LENGTH, 1, CELL_SIDE_LENGTH))
            cell1.wallLeft = True
            cell2.wallRight = True
    elif cell1.x == cell2.x: 
        # cells are above or below eachother
        if cell1.y < cell2.y: 
            # cell1 is above cell2
            pygame.draw.rect(screen, (0, 0, 0), (cell1.x * CELL_SIDE_LENGTH, cell2.y * CELL_SIDE_LENGTH, CELL_SIDE_LENGTH, 1))
            cell1.wallBelow = True
            cell2.wallAbove = True
        else:
            # cell1 is below cell2
            pygame.draw.rect(screen, (0, 0, 0), (cell2.x * CELL_SIDE_LENGTH, cell1.y * CELL_SIDE_LENGTH, CELL_SIDE_LENGTH, 1))
            cell1.wallAbove = True
            cell2.wallBelow = True

